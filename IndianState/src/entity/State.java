package entity;

public class State {
	private String stateName;
	private String stateCapitalName;
	private String CMName;
	private String deputyCMName;
	private int totalMLA;
	private int totalDistrict;
	private int totalTaluka;
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getStateCapitalName() {
		return stateCapitalName;
	}
	public void setStateCapitalName(String stateCapitalName) {
		this.stateCapitalName = stateCapitalName;
	}
	public String getCMName() {
		return CMName;
	}
	public void setCMName(String cMName) {
		CMName = cMName;
	}
	public String getDeputyCMName() {
		return deputyCMName;
	}
	public void setDeputyCMName(String deputyCMName) {
		this.deputyCMName = deputyCMName;
	}
	public int getTotalMLA() {
		return totalMLA;
	}
	public void setTotalMLA(int totalMLA) {
		this.totalMLA = totalMLA;
	}
	public int getTotalDistrict() {
		return totalDistrict;
	}
	public void setTotalDistrict(int totalDistrict) {
		this.totalDistrict = totalDistrict;
	}
	public int getTotalTaluka() {
		return totalTaluka;
	}
	public void setTotalTaluka(int totalTaluka) {
		this.totalTaluka = totalTaluka;
	}
	
}


	

